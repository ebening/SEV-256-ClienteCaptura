package com.adinfi.sevCaptura.entities;

public class Empresa {
	private int id;
	private String nombre;
	
	public Empresa(String nombre){
		this.nombre=nombre;
	}
	public Empresa(){
		
	}
	public int  getId(){
		return this.id;
	}
	
	public void setId(int id){
		this.id=id;
	}
	
	public String getNombre(){
		return this.nombre;
	}
	
	public void setNombre(String nombre){
		this.nombre=nombre;
	}
	
	
	

}
