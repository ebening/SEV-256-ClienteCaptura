package com.adinfi.sevCaptura.entities;

import com.adinfi.sevCaptura.controller.CapturaGastosController;
import com.adinfi.sevCaptura.model.CapturaGastosModel;
import com.adinfi.sevCaptura.view.CapturaGastosView;

public class VentanaCapturaGastos {

	private CapturaGastosModel model;
	private CapturaGastosView view;
	private CapturaGastosController controller;
	
	public VentanaCapturaGastos(){};
	
	/**
	 * 
	 * @param model
	 * @param view
	 * @param controller
	 */
	public VentanaCapturaGastos(CapturaGastosModel model, CapturaGastosView view, 
			CapturaGastosController controller){
		this.model = model;
		this.view = view;
		this.controller = controller;
	}
	
	/**
	 * @return the model
	 */
	public CapturaGastosModel getModel() {
		return model;
	}
	
	/**
	 * @param model the model to set
	 */
	public void setModel(CapturaGastosModel model) {
		this.model = model;
	}

	/**
	 * @return the view
	 */
	public CapturaGastosView getView() {
		return view;
	}

	/**
	 * @param view the view to set
	 */
	public void setView(CapturaGastosView view) {
		this.view = view;
	}

	/**
	 * @return the controller
	 */
	public CapturaGastosController getController() {
		return controller;
	}

	/**
	 * @param controller the controller to set
	 */
	public void setController(CapturaGastosController controller) {
		this.controller = controller;
	}
	
	
}
